    // Method that cumputres the difference of 2 values and display it to a textArea
    private void Subtract(Double a, Double b){
        // Rounds the result of subtracting 2 variables to 3 decimal places
        Double ans = Math.round((a - b)*1000)/1000.0;
        
        method7.setText(Double.toString(ans)); // turns the double to string
    }
    
