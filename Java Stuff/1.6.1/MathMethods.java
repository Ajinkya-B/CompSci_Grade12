    private void RunBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RunBtnActionPerformed
        try{
            // Declaring "Double" type variables
            Double maxNum1, maxNum2, minNum1, minNum2, rootNum, getAbs, addNum1, addNum2, subNum1, subNum2;
            
            
            // Initializing the varibles(Assigns variables to GUI user Input, that is first turned to Double from String) 
            maxNum1 = Double.parseDouble(this.maxNum1.getText());
            maxNum2 = Double.parseDouble(this.maxNum2.getText());
            minNum1 = Double.parseDouble(this.minNum1.getText());
            minNum2 = Double.parseDouble(this.minNum2.getText());
            rootNum = Double.parseDouble(this.rootNum.getText());
            getAbs = Double.parseDouble(this.getAbs.getText());
            addNum1 = Double.parseDouble(this.addNum1.getText());
            addNum2 = Double.parseDouble(this.addNum2.getText());
            subNum1 = Double.parseDouble(this.subNum1.getText());
            subNum2 = Double.parseDouble(this.subNum2.getText());

            
            // Random integer between 0 to 50        
            method1.setText(Integer.toString((int)(Math.random()*50)));

            // Returs the max between 2 variables
            method2.setText(Double.toString(Math.max(maxNum1, maxNum2))); 

            // Returs the min between 2 variables
            method3.setText(Double.toString(Math.min(minNum1, minNum2))); 

            // Returns square root of a variable
            method4.setText(Double.toString(Math.sqrt(rootNum)));

            // Returns absolute value of a variable
            method5.setText(Double.toString(Math.abs(getAbs)));

            // Returns the sum of two variables
            method6.setText(Double.toString(Add(addNum1,addNum2)));

            // Returns the sum of two variables
            Subtract(subNum1, subNum2);

            statusDisplay.setText("SUCCESSFUL!");
        
        }catch(IllegalArgumentException e){
            statusDisplay.setText("FAILED!");
            System.out.println("Please Enter A Number In All Text Fields!");
        }catch(Exception e){
            statusDisplay.setText("FAILED!");
            System.out.println("There Was An Error!");
        }
    }//GEN-LAST:event_RunBtnActionPerformed

