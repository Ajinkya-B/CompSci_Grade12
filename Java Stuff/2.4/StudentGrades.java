    private void listAll(){
        try{
            
            displayDB.setText("");
            
            
            // Goes through each element in the 2d array and appends each 
            // element to the text area
            for(int i=0; i<count; i++){
                for(int j=0; j<db[0].length; j++){
                    if(j == 0){
                        displayDB.append(db[i][j] + ": ");
                    }else{
                        displayDB.append(db[i][j] + ", ");
                    }
                }
                displayDB.append("\n");
            }
            
            
            statusDisplay.setText("SUCCESSFUL");
            
        }catch(Exception e){
            statusDisplay.setText("FAILED");
            System.out.println("There Was An Unknown Error!");
        }
    }
    
    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {                                       
        // TODO add your handling code here:
        try{
        
            String fName, lName, test1, test2, test3, test4;
            double t1, t2, t3, t4;


            // Getting values from the user
            fName = usrFName.getText().trim();
            lName = usrLName.getText().trim();
            test1 = tst1.getText();
            test2 = tst2.getText();
            test3 = tst3.getText();
            test4 = tst4.getText();


            // Checking if the user given values ar valid doubles through 
            // exception handling, by conveting String to Doubles
            t1 = Double.parseDouble(test1) * 100;
            t2 = Double.parseDouble(test2) * 100;
            t3 = Double.parseDouble(test3) * 100;
            t4 = Double.parseDouble(test4) * 100;
            
            if (!"".equals(fName)){
                // check to make sure if all test scores are upto 2 decimal places
                if((t1-(int)t1) == 0 && (t2-(int)t2) == 0 && (t3-(int)t3) == 0 && (t4-(int)t4) == 0){

                    // checks for any negative mark
                    if(t1>=0 && t1/100<=100 && t2>=0 && t2/100<=100 && t3>=0 && t3/100<=100 && t4>=0 && t4/100<=100){

                        // Finally adding values to array
                        db[count][0] = fName + " " + lName;
                        db[count][1] = test1;
                        db[count][2] = test2;
                        db[count][3] = test3;
                        db[count][4] = test4; // [[name, t1, t1, t3, t4],[...], ....]
                        count ++;

                        
                        listAll();

                    }else{
                        statusDisplay.setText("FAILED");
                        System.out.println("Please Enter A Valid Test Mark!");
                    }
                }else {
                    statusDisplay.setText("FAILED!");
                    System.out.println("Please Enter An Amount In Correct Format!");
                }
            }else{
                statusDisplay.setText("FAILED!");
                System.out.println("Please Fill The First And Last Name!");
            }
            
            
        }catch(IllegalArgumentException e){
            statusDisplay.setText("FAILED");
            System.out.println("Please Enter A Number As A Test Mark!");
        }catch(ArrayIndexOutOfBoundsException e){
            statusDisplay.setText("FAILED");
            System.out.println("Maximum Capacity Reached For The Student Database!");
        }catch(Exception e){
            statusDisplay.setText("FAILED");
            System.out.println("There Was An Unknown Error!");
        }
    }                                      

