    private void studentAvgActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        try{
            String userWName, uF, uL;
            double average, total;

            
            displayDB.setText("");
            uF = usrFName.getText().trim();
            uL = usrLName.getText().trim();
            
            
            // checks if user entered student name
            if (!"".equals(uF)){
                userWName = uF + " " + uL;

                for(int i=0; i<count; i++){   
                    if(db[i][0].equals(userWName)){ // ckecks if the array contains student name

                        total = 0; // initilizing total to zero in here in case there are multiple students with same name
                        displayDB.append(userWName + ": ");

                        for(int j=1; j<db[0].length; j++){
                            if(j == 0){
                                displayDB.append(db[i][j] + ": ");
                            }else{
                                displayDB.append(db[i][j] + ", ");
                            }
                            total += Double.parseDouble(db[i][j]); // incrementing the total by each test mark of the student
                        }


                        average = total/4;
                        displayDB.append("\nAvg: " + Double.toString(Math.round (average*100.0)/100.0) + "\n\n");
                    }
                }
            statusDisplay.setText("SUCCESSFUL");
            }else{
                statusDisplay.setText("FAILED");
                System.out.println("Please Fill The First And Last Name!");
            }
            
        }catch(Exception e){
            statusDisplay.setText("FAILED");
            System.out.println("There Was An Unknown Error!");
        }
    }                                          

