    private void courseAvgActionPerformed(java.awt.event.ActionEvent evt) {                                          
        // TODO add your handling code here:
        try{
            
            double average1, average2, average3, average4, total1, total2, total3, total4;
            String t = "";
            
            
            total1 = 0;
            total2 = 0;
            total3 = 0;
            total4 = 0;
            displayDB.setText("");
            
            
            for(int i=0; i<count; i++){
                for(int j=0; j<db[0].length; j++){
                    
                    if(j == 0){
                        t += db[i][j] + ": ";
                    }else{
                        t += db[i][j] + ", ";
                    }
                    
                    // adds mark to the specific total var
                    // (test1 --> total1, test2 --> total2, ...)
                    switch (j) {
                        case 1:
                            total1 += Double.parseDouble(db[i][j]);
                            break;
                        case 2:
                            total2 += Double.parseDouble(db[i][j]);
                            break;
                        case 3:
                            total3 += Double.parseDouble(db[i][j]);
                            break;
                        case 4:
                            total4 += Double.parseDouble(db[i][j]);
                            break;
                        default:
                            break;
                    }
                }
                t += "\n";
            }

            
            average1 = total1/count;
            average2 = total2/count;
            average3 = total3/count;
            average4 = total4/count;

            
            // Output averages and all student info to the textArea
            displayDB.append("Test1 Avg: " + Double.toString(Math.round (average1*100.0)/100.0) + "\n");
            displayDB.append("Test2 Avg: " + Double.toString(Math.round (average2*100.0)/100.0) + "\n");
            displayDB.append("Test3 Avg: " + Double.toString(Math.round (average3*100.0)/100.0) + "\n");
            displayDB.append("Test4 Avg: " + Double.toString(Math.round (average4*100.0)/100.0) + "\n\n");
            displayDB.append(t);
            statusDisplay.setText("SUCCESSFUL");
            
        }catch(Exception e){
            statusDisplay.setText("FAILED");
            System.out.println("There Was An Unknown Error!");
        }
        
    }                                         

